using System;

namespace CallingMethodsAssignment
{
    public class MathMethods
    {
        // Method to add 10 to the input parameter
        public static int AddTen(int num)
        {
            return num + 10;
        }

        // Method to multiply the input parameter by 2
        public static int MultiplyByTwo(int num)
        {
            return num * 2;
        }

        // Method to subtract 5 from the input parameter
        public static int SubtractFive(int num)
        {
            return num - 5;
        }
    }
}

